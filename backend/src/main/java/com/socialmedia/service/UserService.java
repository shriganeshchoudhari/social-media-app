    @Transactional
    public void followUser(Long userId) {
        User currentUser = getCurrentUser();
        User userToFollow = userRepository.findById(userId)
                .orElseThrow(() -> new ResourceNotFoundException("User not found"));

        if (currentUser.getId().equals(userId)) {
            throw new RuntimeException("Cannot follow yourself");
        }

        if (!followRepository.existsByFollowerAndFollowing(currentUser, userToFollow)) {
            Follow follow = new Follow(currentUser, userToFollow, "accepted");
            followRepository.save(follow);

            currentUser.setFollowingCount(currentUser.getFollowingCount() + 1);
            userToFollow.setFollowersCount(userToFollow.getFollowersCount() + 1);
            userRepository.save(currentUser);
            userRepository.save(userToFollow);
        }
    }

    @Transactional
    public void unfollowUser(Long userId) {
        User currentUser = getCurrentUser();
        User userToUnfollow = userRepository.findById(userId)
                .orElseThrow(() -> new ResourceNotFoundException("User not found"));

        if (followRepository.existsByFollowerAndFollowing(currentUser, userToUnfollow)) {
            followRepository.deleteByFollowerAndFollowing(currentUser, userToUnfollow);

            currentUser.setFollowingCount(Math.max(0, currentUser.getFollowingCount() - 1));
            userToUnfollow.setFollowersCount(Math.max(0, userToUnfollow.getFollowersCount() - 1));
            userRepository.save(currentUser);
            userRepository.save(userToUnfollow);
        }
    }

